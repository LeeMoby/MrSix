public class CabinetBean {
    private String cabinetID;
    private String cabinetNO;
    private String cabinetQRCode;
    private String cabinetX;
    private String cabinetY;

    public String getCabinetID() {
        return cabinetID;
    }

    public void setCabinetID(String cabinetID) {
        this.cabinetID = cabinetID;
    }

    public String getCabinetNO() {
        return cabinetNO;
    }

    public void setCabinetNO(String cabinetNO) {
        this.cabinetNO = cabinetNO;
    }

    public String getCabinetQRCode() {
        return cabinetQRCode;
    }

    public void setCabinetQRCode(String cabinetQRCode) {
        this.cabinetQRCode = cabinetQRCode;
    }

    public String getCabinetX() {
        return cabinetX;
    }

    public void setCabinetX(String cabinetX) {
        this.cabinetX = cabinetX;
    }

    public String getCabinetY() {
        return cabinetY;
    }

    public void setCabinetY(String cabinetY) {
        this.cabinetY = cabinetY;
    }
}
